using Microsoft.EntityFrameworkCore;
using StudentManagmentSystem.DAL.Data;
using StudentManagmentSystem.DAL.Repository;
using StudentManagmentSystem.DAL.Repository.Interface;

namespace StudentManagmentSystem.WebUI
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.
            builder.Services.AddControllersWithViews();
            builder.Services.AddRazorPages().AddRazorRuntimeCompilation();
            builder.Services.AddDbContext<CustomDbContext>(opts =>
            {
                opts.UseSqlServer(builder.Configuration.GetConnectionString("SqlServerConnectionString"));
            });

            builder.Services.AddScoped<IStudentRepository, StudentRepository>();

            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (!app.Environment.IsDevelopment())
            {
                app.UseExceptionHandler("/Home/Error");
                // The default HSTS value is 30 days. You may want to change this for production scenarios, see https://aka.ms/aspnetcore-hsts.
                app.UseHsts();
            }

            app.UseHttpsRedirection();
            app.UseStaticFiles();

            app.UseRouting();

            app.UseAuthorization();

            app.MapControllerRoute(
                name: "default",
                pattern: "{controller=Home}/{action=Index}/{id?}");

            app.Run();
        }
    }
}
